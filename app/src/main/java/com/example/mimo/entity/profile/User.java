public class User {
    private String email;
    private String name;
    private String picture;
    private String nickname;

    public User() {
    }

    public String getEmail() {
        return email;
    }

    public String getName() {
        return name;
    }

    public String getPicture() {
        return picture;
    }

    public String getNickname() {
        return nickname;
    }
}
